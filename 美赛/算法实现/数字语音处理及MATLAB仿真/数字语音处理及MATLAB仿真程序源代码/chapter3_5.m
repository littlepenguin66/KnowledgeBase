fid=fopen('zqq.txt','rt')                %读入语音文件
x=fscanf(fid,'%f')
fclose(fid)

s=fra(50,50,x)                       %语音短时平均幅度图
s3=abs(s)
avap=sum(s3,2)
subplot(2,2,1)
plot(avap)
xlabel('帧数')
ylabel('短时平均幅度 M')
legend('N=50')
axis([0,1500,0,10*10^5])
